package com.company;

public class Code_1_Literal {

    public static void main(String[] args) {
        byte age = 34; //integer literal
        char ch = 'A';  //integer literal
        short age2 = 83; //integer literal
        long ageDino = 5666666666L; //long literal
        float f1 = 5.6f; //float literal
        double d1 = 4.66d; //double literal
        boolean a = true; //boolean literal
        String str = "Code"; //string Literal
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(age2);
        System.out.println(ageDino);
        System.out.println(a);
        System.out.println(str);
    }
}
